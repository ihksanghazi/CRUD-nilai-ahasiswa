    private void B_InputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_InputActionPerformed
        // TODO add your handling code here:
        String nim = TF_NIM.getText();
        String nama = TF_Nama.getText();
        int harian = Integer.parseInt(TF_NilaiHarian.getText());
        int uts = Integer.parseInt(TF_NilaiUTS.getText());
        int uas = Integer.parseInt(TF_NilaiUAS.getText());
        int rata_rata = (harian*40/100)+(uts*30/100)+(uas*30/100);
        String hasil = rata_rata > 60 ? "LULUS" : "TIDAK LULUS";
        SQL("INSERT INTO mahasiswa VALUES(DEFAULT,"+nim+",'"+nama+"',"+harian+","+uts+","+uas+","+rata_rata+",'"+hasil+"')");
        load_table("");
        kosong();
    }//GEN-LAST:event_B_InputActionPerformed

    private void Table_MahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_MahasiswaMouseClicked
        // TODO add your handling code here:
        int baris = Table_Mahasiswa.rowAtPoint(evt.getPoint());
        
        String nim = Table_Mahasiswa.getValueAt(baris, 0).toString();
        String nama = Table_Mahasiswa.getValueAt(baris, 1).toString();
        String harian = Table_Mahasiswa.getValueAt(baris, 2).toString();
        String uts = Table_Mahasiswa.getValueAt(baris, 3).toString();
        String uas = Table_Mahasiswa.getValueAt(baris, 4).toString();
        
        TF_NIM.setText(nim);
        TF_Nama.setText(nama);
        TF_NilaiHarian.setText(harian);
        TF_NilaiUTS.setText(uts);
        TF_NilaiUAS.setText(uas);
        try {
            read("SELECT ID FROM mahasiswa WHERE nim = "+nim+"");
            this.Rs.next();
            this.id = this.Rs.getInt(1);
        } catch (Exception e) {
            System.out.println("Error = "+e.getMessage());
        }
        
    }//GEN-LAST:event_Table_MahasiswaMouseClicked

