    private static Connection hasilKoneksi;
    private ResultSet Rs;
    public static Connection koneksi() throws Exception{
        try {
            String url ="jdbc:mysql://localhost:3306/db_mahasiswa?zeroDateTimeBehavior=convertToNull";
            String user = "root";
            String pass = "";
            DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            hasilKoneksi=DriverManager.getConnection(url,user,pass);
        } catch (Exception e) {
            System.err.println("Koneksi Gagal = " + e.getMessage());
        }
        return hasilKoneksi;
    }
    
    public void SQL(String query){
        try {
            Connection conn = koneksi();
            PreparedStatement pst = conn.prepareStatement(query);
            pst.execute();
        } catch (Exception e) {
            System.err.println("Query Gagal = "+e.getMessage());
        }
    }
    
    public void read(String query){    
        try {
            Connection conn = koneksi();
            Statement Stm = conn.createStatement();
            Rs = Stm.executeQuery(query);
        } catch (Exception e) {
            System.err.println("Gagal Dibaca = " + e.getMessage());
        }
    }
    
    // Fungsi Untuk Menampilkan Data Kedalam Table
    private void load_table(String query){
        if(query.isEmpty()){
            query = "SELECT * FROM mahasiswa";
        }
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIM");
        model.addColumn("NAMA");
        model.addColumn("Nilai Harian");
        model.addColumn("Nilai UTS");
        model.addColumn("Nilau UAS");
        model.addColumn("Rata Rata");
        model.addColumn("Hasil");
        try {
            read(query);
            while (this.Rs.next()) {
               model.addRow(new Object[]{this.Rs.getString(2),this.Rs.getString(3),this.Rs.getString(4),this.Rs.getString(5),this.Rs.getString(6),this.Rs.getString(7),this.Rs.getString(8)});
            }
        } catch (Exception e) {
        }
        
        Table_Mahasiswa.setModel(model);
    }
    
